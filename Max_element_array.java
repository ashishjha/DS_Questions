import java.util.*;
public class Max_element_array {

	public static void main(String args[])
	{
		Scanner obj = new Scanner(System.in);
		int i,n,a[],max;
		n = obj.nextInt();
		a = new int[n];
		for(i=0;i<n;i++) 
		{
			a[i] = obj.nextInt();
		}
		max = a[0];
		for(i=0;i<n;i++)
		{
			if(a[i]>max)
			{
				max = a[i];
			}
		}
		System.out.println("Max element = " + max);
			
	}
}
